#!/usr/bin/env node
/**
 * Generates src/assets/bankLogos.ts from the SVG files in assets/bank-logos/.
 * Each file is named `Bank Name=<name>.svg`. We emit one import per file plus a
 * lookup keyed by a normalized bank name, so the UI can resolve a logo from an
 * arbitrary institution name. Re-run after adding/removing logo files:
 *   node scripts/gen-bank-logos.js
 */
const fs = require('fs');
const path = require('path');

const LOGO_DIR = path.join(__dirname, '..', 'assets', 'bank-logos');
const OUT = path.join(__dirname, '..', 'src', 'assets', 'bankLogos.ts');

// Mirror of the `norm` helper in BankLogo — keep the two in sync.
function norm(s) {
  return s
    .normalize('NFKD')
    .replace(/[̀-ͯ]/g, '')
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]/g, '');
}

const files = fs
  .readdirSync(LOGO_DIR)
  .filter((f) => f.toLowerCase().endsWith('.svg'))
  .sort();

const entries = files.map((file, i) => {
  const raw = file.replace(/\.svg$/i, '').replace(/^Bank Name=/i, '').trim();
  return { file, name: raw, key: norm(raw), id: `L${i}` };
});

// Guard against two files normalizing to the same key (first one wins).
const seen = new Map();
for (const e of entries) {
  if (seen.has(e.key)) {
    console.warn(`⚠︎  duplicate key "${e.key}": "${e.name}" and "${seen.get(e.key)}"`);
  } else {
    seen.set(e.key, e.name);
  }
}

const imports = entries
  .map((e) => `import ${e.id} from '../../assets/bank-logos/${e.file}';`)
  .join('\n');

const rows = entries.map((e) => `  '${e.key}': ${e.id},`).join('\n');

// Display names, de-duplicated by normalized key (first file wins) and sorted.
const names = [...seen.values()].sort((a, b) => a.localeCompare(b));
const nameRows = names.map((n) => `  '${n.replace(/'/g, "\\'")}',`).join('\n');

const out = `// AUTO-GENERATED by scripts/gen-bank-logos.js — do not edit by hand.
// Source: assets/bank-logos/*.svg  (${entries.length} logos)
import type { FC } from 'react';
import type { SvgProps } from 'react-native-svg';

${imports}

/** Normalized bank name → logo component. See BankLogo for the matching rules. */
export const BANK_LOGOS: Record<string, FC<SvgProps>> = {
${rows}
};

/** Display names of every bank we have a logo for (sorted) — powers the picker. */
export const BANK_NAMES: string[] = [
${nameRows}
];
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, out);
console.log(`Wrote ${OUT} with ${entries.length} logos.`);
